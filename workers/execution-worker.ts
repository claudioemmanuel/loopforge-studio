// Calculate execution progress based on phase, iteration and event type
// Setup phase: 0-20%, Execution phase: 20-95%, Complete: 100%
function calculateExecutionProgress(
  phase: "setup" | "execution",
  iteration: number,
  maxIterations: number,
  eventType: string,
): number {
  // Complete events get 100%
  if (eventType === "complete" || eventType === "task_complete") {
    return 100;
  }

  // Setup phase progress (0-20%)
  if (phase === "setup") {
    const setupProgress: Record<string, number> = {
      setup_start: 2,
      repo_clone: 5,
      repo_update: 8,
      branch_create: 15,
      branch_checkout: 15,
      setup_complete: 20,
    };
    return setupProgress[eventType] || 10;
  }

  // Execution phase progress (20-95%)
  const executionBase = 20;
  const executionRange = 75; // 95 - 20

  // Error/stuck events stay at current progress
  if (
    eventType === "error" ||
    eventType === "stuck" ||
    eventType === "task_failed"
  ) {
    return executionBase + (iteration / maxIterations) * executionRange * 0.9;
  }

  // Calculate progress within execution phase (20% to 95%)
  const iterationProgress = Math.min(iteration / maxIterations, 1);
  return Math.min(executionBase + iterationProgress * executionRange, 95);
}

// Build authenticated clone URL with GitHub token
function buildAuthenticatedCloneUrl(cloneUrl: string, token: string): string {
  const url = new URL(cloneUrl);
  url.username = "x-access-token";
  url.password = token;
  return url.toString();
}

// Get user's decrypted GitHub token
async function getUserGithubToken(userId: string): Promise<string | null> {
  const user = await db.query.users.findFirst({
    where: eq(users.id, userId),
  });

  if (!user?.encryptedGithubToken || !user?.githubTokenIv) {
    return null;
  }

  try {
    return decryptGithubToken({
      encrypted: user.encryptedGithubToken,
      iv: user.githubTokenIv,
    });
  } catch (error) {
    workerLogger.error({ error }, "Failed to decrypt GitHub token");
    return null;
  }
}

// Ensure target branch exists, creating from default branch if needed
async function ensureBranchExists(
  repoPath: string,
  targetBranch: string,
  defaultBranch: string,
  githubToken: string | null,
): Promise<{ created: boolean }> {
  const git = simpleGit(repoPath);

  // Fetch all remote branches
  await git.fetch("origin");

  // Check if target branch exists on remote
  const remotes = await git.branch(["-r"]);
  const remoteBranchExists = remotes.all.includes(`origin/${targetBranch}`);

  if (remoteBranchExists) {
    // Branch exists, checkout and pull
    workerLogger.info({ targetBranch }, "Branch exists, checking out");
    await git.checkout(targetBranch);
    await git.pull("origin", targetBranch);
    return { created: false };
  } else {
    // Branch doesn't exist, create from default branch
    workerLogger.info(
      { targetBranch, defaultBranch },
      "Branch does not exist, creating from default",
    );

    // First checkout default branch and pull latest
    await git.checkout(defaultBranch);
    await git.pull("origin", defaultBranch);

    // Create new branch from default (don't push yet - wait until we have commits)
    await git.checkoutLocalBranch(targetBranch);
    workerLogger.info(
      { targetBranch },
      "Created local branch (will push when commits are made)",
    );

    return { created: true };
  }
}

// Clone or update repository
async function cloneOrUpdateRepo(
  cloneUrl: string,
  repoName: string,
  targetBranch: string,
  defaultBranch: string,
  githubToken: string | null,
): Promise<{ repoPath: string; branchCreated: boolean }> {
  const repoPath = path.join(REPOS_DIR, repoName);

  // Use authenticated URL if token available
  const effectiveCloneUrl = githubToken
    ? buildAuthenticatedCloneUrl(cloneUrl, githubToken)
    : cloneUrl;

  // Ensure repos directory exists
  await fs.mkdir(REPOS_DIR, { recursive: true });

  const git = simpleGit();

  // Check if repo already exists
  let repoExists = false;
  try {
    await fs.access(repoPath);
    repoExists = true;
  } catch {
    repoExists = false;
  }

  if (repoExists) {
    // Repo exists, fetch latest
    workerLogger.info({ repoPath }, "Repository exists, fetching latest");
    const repoGit = simpleGit(repoPath);
    await repoGit.fetch("origin");
  } else {
    // Clone from default branch first (always succeeds)
    workerLogger.info({ repoName, defaultBranch }, "Cloning repository");
    await git.clone(effectiveCloneUrl, repoPath, ["--branch", defaultBranch]);
  }

  // Now ensure target branch exists (creates if needed)
  const { created } = await ensureBranchExists(
    repoPath,
    targetBranch,
    defaultBranch,
    githubToken,
  );

  return { repoPath, branchCreated: created };
}

