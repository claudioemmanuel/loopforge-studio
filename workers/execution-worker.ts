// Parse boolean environment variable
function parseBoolean(
  value: string | undefined,
  defaultValue: boolean,
): boolean {
  if (value === undefined) return defaultValue;
  const normalized = value.toLowerCase().trim();
  return (
    normalized === "true" ||
    normalized === "1" ||
    normalized === "yes" ||
    normalized === "on"
  );
}

// Build PR description from task and execution result
function buildPRDescription(
  taskTitle: string,
  taskDescription: string,
  result: { iterations: number; commits?: string[]; error?: string },
): string {
  const lines: string[] = [
    "## Summary",
    "",
    `This PR implements: **${taskTitle}**`,
    "",
  ];

  if (taskDescription) {
    lines.push(taskDescription, "");
  }

  lines.push("## Changes");
  lines.push("");
  lines.push(`- Completed in ${result.iterations} iteration(s)`);
  if (result.commits && result.commits.length > 0) {
    lines.push(`- ${result.commits.length} commit(s) made`);
  }
  lines.push("");

  lines.push("## Test Plan");
  lines.push("");
  lines.push("- [ ] Verify the changes work as expected");
  lines.push("- [ ] Run existing tests");
  lines.push("- [ ] Manual testing completed");
  lines.push("");

  lines.push("---");
  lines.push("*Generated by [LoopForge](https://loopforge.dev)*");

  return lines.join("\n");
}

// Centralized error handling for execution failures
async function handleExecutionFailure(
  error: Error | string,
  context: {
    executionId: string;
    taskId: string;
    userId: string;
    workerJobId?: string;
    repoName?: string;
    taskTitle?: string;
  },
): Promise<void> {
  const errorMessage = error instanceof Error ? error.message : error;

  workerLogger.error(
    { ...context, error: errorMessage },
    "Execution failure - updating all records",
  );

  // Update execution status
  await db
    .update(executions)
    .set({
      status: "failed",
      completedAt: new Date(),
      errorMessage,
    })
    .where(eq(executions.id, context.executionId));

  // Update task status with history
  await db
    .update(tasks)
    .set({
      status: "stuck",
      statusHistory: buildStatusHistoryAppend({
        fromStatus: "executing",
        toStatus: "stuck",
        triggeredBy: "worker",
      }),
      processingPhase: null,
      processingJobId: null,
      processingStartedAt: null,
      processingStatusText: null,
      updatedAt: new Date(),
    })
    .where(eq(tasks.id, context.taskId));

  // Update worker job if we have one
  if (context.workerJobId) {
    await db
      .update(workerJobs)
      .set({
        status: "failed",
        completedAt: new Date(),
        errorMessage,
      })
      .where(eq(workerJobs.id, context.workerJobId));

    // Emit error event to worker_events
    await db.insert(workerEvents).values({
      workerJobId: context.workerJobId,
      eventType: "error",
      content: errorMessage,
    });
  }

  // Publish error event to Redis for SSE
  if (context.repoName && context.taskTitle) {
    const errorEvent = createWorkerUpdateEvent(
      context.taskId,
      context.taskTitle,
      context.repoName,
      "stuck",
      {
        currentAction: "Execution failed",
        error: errorMessage,
        completedAt: new Date(),
      },
    );
    await publishWorkerEvent(context.userId, errorEvent);
  }
}

